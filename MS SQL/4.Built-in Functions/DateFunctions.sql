--Datepart
SELECT DATEPART(DAY,'05-27-2024') AS [Day]
SELECT DATEPART(MONTH,'05-27-2024') AS [Month]
SELECT DATEPART(YEAR,'05-27-2024') AS [Year]


SELECT DATEPART(WEEKDAY,'05-27-2024') AS [Weekday]

--Datediff
SELECT DATEDIFF(DAY,'01-02-2021','01-02-2024') AS [Day difference]
SELECT DATEDIFF(YEAR,'01-02-2021','01-02-2024') AS [Year difference]
SELECT DATEDIFF(MONTH,'01-02-2021','01-02-2024') AS [Month difference]

--Datename
SELECT DATENAME(WEEKDAY,GETDATE())

SELECT DATENAME(WEEKDAY,'12-21-1987') --Monday
SELECT DATENAME(WEEKDAY,'04-04-1991') --Thursday

--Dateadd
SELECT DATEADD(DAY,20,'02-03-2024')

--Get date
SELECT GETDATE()

--End of the month
SELECT EOMONTH(GETDATE())
SELECT EOMONTH('01-03-2023')